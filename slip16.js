// Callback function to be triggered on button click
function buttonClick() {
    console.log('Button clicked!');
}

// Add an event listener to the button
document.getElementById('myButton').addEventListener('click', buttonClick);

// Main loop (in this simple example, it's not a true "main loop" as in game development)
console.log('Event-driven application is running...');
